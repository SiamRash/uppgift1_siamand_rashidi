package Uppgift2.uppgift2_1;

public class Hund {
    private String name;
    private String ras;

    public Hund(String name, String ras) {
        this.name = name;
        this.ras = ras;
    }

    public String getName() {
        return name;
    }

    public String getRas() {
        return ras;
    }
}
